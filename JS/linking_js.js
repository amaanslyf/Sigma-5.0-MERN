// Arithmetic Operators
let a = 10, b = 5;
console.log("Addition:", a + b);      // 15
console.log("Subtraction:", a - b);   // 5
console.log("Multiplication:", a * b); // 50
console.log("Division:", a / b);      // 2
console.log("Modulus:", a % b);       // 0
console.log("Exponentiation:", a ** b); // 10^5 = 100000

// Unary Operators
let c = 5;
console.log("Unary Plus:", +c);   // 5
console.log("Unary Negation:", -c); // -5
console.log("Increment:", ++c);   // 6 (Pre-increment)
console.log("Decrement:", --c);   // 5 (Pre-decrement)

// Comparison Operators
console.log("Equal:", a == b);    // false
console.log("Strict Equal:", a === b); // false (checks type too)
console.log("Not Equal:", a != b); // true
console.log("Greater than:", a > b); // true
console.log("Less than:", a < b); // false

// Assignment Operators
let x = 10;
x += 5;  // x = x + 5
console.log("Assignment Addition:", x); // 15
x *= 2;  // x = x * 2
console.log("Assignment Multiplication:", x); // 30

// Logical Operators
let isTrue = true, isFalse = false;
console.log("AND Operator:", isTrue && isFalse); // false
console.log("OR Operator:", isTrue || isFalse); // true
console.log("NOT Operator:", !isTrue); // false
